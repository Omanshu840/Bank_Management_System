    private void LogInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LogInActionPerformed

        int f = 0;
        try
        {
            Class.forName("java.sql.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/omibbank","root","");
            Statement stmt = con.createStatement();
            String userid = jtxtuserid.getText();
            String password = jpassword.getText();
            String query = "select * from login;";
            ResultSet rs = stmt.executeQuery(query);
            while(rs.next())
            {
                String u = rs.getString("Userid");
                String p = rs.getString("password");
                
                if(u.equals(userid) && p.equals(password))
                {
                    f = f+1;
                }
            }
            if(f!=0)
            {
                selecttype info = new selecttype();
                info.setVisible(true);
                
                dispose();
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Invalid Login Details","Login Error",JOptionPane.ERROR_MESSAGE);
                jtxtuserid.setText(null);
                jpassword.setText(null);
            }
            rs.close();
            stmt.close();
            con.close();
        }
        catch(Exception e)
        {
            System.out.print(e);
        }

    }//GEN-LAST:event_LogInActionPerformed

